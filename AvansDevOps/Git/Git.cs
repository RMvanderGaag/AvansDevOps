namespace AvansDevOps;

public class Git
{
    public void Commit()
    {
        Console.WriteLine("Committing changes to the repository.");
    }
    
    public void Push()
    {
        Console.WriteLine("Pushing changes to the repository.");
    }
    
    public void Pull()
    {
        Console.WriteLine("Pulling changes from the repository.");
    }
    
    public void Merge()
    {
        Console.WriteLine("Merging changes from the repository.");
    }
    
    public void Branch()
    {
        Console.WriteLine("Creating a new branch in the repository.");
    }
}